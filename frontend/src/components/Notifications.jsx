import { useEffect, useState } from 'react';
import useNotification from '../hook/useNotification';

function Notification({ data, onClosedNotification }) {
  const [isShow, setIsShow] = useState(false);
  useEffect(() => {
    if (data) {
      setIsShow(true);
      const timeoutId = setTimeout(() => {
        setIsShow(false);
        onClosedNotification();
      }, 10000);
      return () => clearTimeout(timeoutId);
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [data]);

  const handleCloseNotification = (e) => {
    setIsShow(false);
    onClosedNotification();
  };

  if (!data) return null;
  return (
    <div
      className={`w-96 mb-3 p-4 bg-white rounded-lg shadow border border-gray-200 justify-start items-start inline-flex
    transition-all duration-300 ease-in-out
    ${isShow ? 'translate-x-0' : 'translate-x-full'}`}
    >
      <NotificationIcon status={data.status} />
      <div className="grow shrink basis-0 px-3 flex-col justify-start items-start gap-1 inline-flex">
        <div className="self-stretch text-gray-500 text-sm font-normal leading-tight">
          {data.message}
        </div>
      </div>
      <div className="w-5 h-5 relative cursor-pointer" onClick={handleCloseNotification}>
        <svg
          width="12"
          height="12"
          viewBox="0 0 12 12"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
        >
          <path
            d="M1.33341 11.8333L0.166748 10.6667L4.83341 6L0.166748 1.33333L1.33341 0.166664L6.00008 4.83333L10.6667 0.166664L11.8334 1.33333L7.16675 6L11.8334 10.6667L10.6667 11.8333L6.00008 7.16666L1.33341 11.8333Z"
            fill="#6B7280"
          />
        </svg>
      </div>
    </div>
  );
}

