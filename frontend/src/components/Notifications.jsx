function Notifications() {
  const { notifications, closeNotification } = useNotification();
  return (
    <div className="fixed z-50 w-96 bottom-0 right-3 flex flex-col items-end overflow-hidden">
      {notifications.map((notification, indexNotification) => {
        return (
          <Notification
            key={indexNotification}
            data={notification}
            onClosedNotification={() => {
              setTimeout(() => {
                closeNotification(indexNotification);
              }, 500);
            }}
          />
        );
      })}
    </div>
  );
}

export default Notifications;
